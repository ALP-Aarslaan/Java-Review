package com.company;

public class ComparisionOperator15 {
    public static void main(String[] args) {
        int x=1;
        int y=1;
        //equal operator (==)
        //unequal operator(!=)
        //greater than or equal operator (>=)
        //less than or equal operator(<=)
        //greater that operator(>)
        //less than operator(<)
        System.out.println(x==y);
        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x!=y);
        System.out.println(x>=y);
        System.out.println(x<=y);
    }
}
