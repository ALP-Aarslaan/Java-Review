public class $_17_relational_operator {
    public static void main(String[] args) {
        int a=10;
        int b=13;
        boolean c=(a<b);//true
        System.out.println("c="+c);
        c=(a<=b);//true
        System.out.println("c="+c);
        c=(a==b);//false
        System.out.println("c="+c);
        c=(a>b);//false
        System.out.println("c="+c);
        c=(a>=b);//false
        System.out.println("c="+c);
        c=(a!=b);//true
        System.out.println("c="+c);
    }
}
