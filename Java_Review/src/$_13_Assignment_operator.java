public class $_13_Assignment_operator {
    public static void main(String[] args) {
        int x=5;
        int y=3;
        x+=y;
        System.out.println(x);
        x-=y;
        System.out.println(x);
        x*=y;
        System.out.println(x);
        x/=y;
        System.out.println(x);
        x%=y;
        System.out.println(x);
    }
}
