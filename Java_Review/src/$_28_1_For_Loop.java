public class $_28_1_For_Loop {
    public static void main(String[] args) {
        int i;
        for(i=1;i<=10;i++){
            System.out.print("Bangladesh");
        }
        for(i=1;i<=100;i=i+2){
            System.out.println(i);
        }
        for(i=10;i>=1;i--){
            System.out.println(i);
        }
    }
}
