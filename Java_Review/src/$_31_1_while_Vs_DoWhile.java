public class $_31_1_while_Vs_DoWhile {
    public static void main(String[] args) {
        int x=1;
        do{
            System.out.println(x+"Bangladesh");
            x++;
        }while(x<=5);
        System.out.println();
        x=6;
        do{
            System.out.println(x+"Bangladesh");
        }while(x<=5);
        System.out.println("\n");
        x=1;
        while(x<=5){
            System.out.println(x+"Bangladesh");
            x++;
        }
        System.out.println();
        x=6;
        while(x<=5){
            System.out.println(x+"Bangladesh");
            x++;
        }
    }
}
