public class $_31_While_Vs_Do_while_loop {
    public static void main(String[] args) {
        int x=1;
        do{
            System.out.println("Bangladesh");
            x++;
        }while(x<=5);
        x=6;
        do {
            System.out.println(x+"Bangladesh");
        }while(x<=5);

        x=1;
        while(x<=5){
            System.out.println(x+"Bangladesh");
            x++;
        }
        x=6;
        while(x<=5){
            System.out.println(x+"Bangladesh");
            x++;
        }
    }
}
